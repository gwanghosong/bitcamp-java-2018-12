// 리터럴(literal) - 자바 언어로 표현한 값
package ch02;

public class Test04 {
  public static void main(String[] args) {
    // 5 - 5 의 결과를 출력하라.
    System.out.println(5 - 5);
    // 5 + (-5)의 결과를 출력하라.
    System.out.println(5 + (-5));
    
  }
}

